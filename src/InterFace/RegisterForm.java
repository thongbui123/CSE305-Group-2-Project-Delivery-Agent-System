    private void doAddUser() {
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        Customer customer = new Customer();
        customer.setUsernameC(txt_UserName.getText());
        customer.setPasswordC(String.valueOf(txt_Password.getPassword()));
        customer.setDateOfBirthC(df.format(jdate_DateOfBirthC.getDate()));
        customer.setEmailC(txt_Email.getText());
        customer.setGenderC((String) CbxGender.getSelectedItem());
        customer.setPhoneNumberC(txt_MobileNumber.getText());
        customer.setAddressC(txt_Address.getText());
        cn.InsertAccountCustomer(customer);
        cn.InsertCustomer(customer);
        this.dispose();
    }
