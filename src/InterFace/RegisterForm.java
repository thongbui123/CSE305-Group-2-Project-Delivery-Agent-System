    private void btn_NextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_NextActionPerformed

        String user, pass, confirmPass;
        boolean checkFill = true;
        boolean checkSamePass = true;

        user = txt_UserName.getText();
        pass = String.valueOf(txt_Password.getPassword());
        confirmPass = String.valueOf(txt_PassConfirm.getPassword());

        if (user.equals("")) {
            txt_UserName.setBorder(BorderFactory.createLineBorder(Color.RED));
            checkFill = false;
        }

        if (pass.equals("")) {
            txt_Password.setBorder(BorderFactory.createLineBorder(Color.RED));
            checkFill = false;
        }

        if (confirmPass.equals("")) {
            txt_PassConfirm.setBorder(BorderFactory.createLineBorder(Color.RED));
            checkFill = false;
        }

        if (!checkFill) {
            JOptionPane.showMessageDialog(this, "Please Fill in All");
            return;
        }

        boolean checkUser = doCheckAccount(user);

        if (!checkUser) {
            JOptionPane.showMessageDialog(this, "This username has already been used");
            return;
        }

        if (!confirmPass.equals(pass)) {
            checkSamePass = false;
        }

        if (!checkSamePass) {
            JOptionPane.showMessageDialog(this, "The password doesn't match");
            return;
        }

        panel_CardLayout.removeAll();;
        panel_CardLayout.add(jPanel2);
        panel_CardLayout.updateUI();
    }//GEN-LAST:event_btn_NextActionPerformed

    private boolean doCheckAccount(String userR) {
        ArrayList<Account> listAccount = cn.getListAccount();

        for (int i = 0; i < listAccount.size(); i++) {
            if (listAccount.get(i).getUsername().equals(userR)) {
                return false;
            }
        }

        return true;
    }
