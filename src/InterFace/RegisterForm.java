    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        String email, gender, phoneNumber, address;
        boolean checkFill = true;

        email = txt_Email.getText();
        gender = (String) CbxGender.getSelectedItem();
        phoneNumber = txt_MobileNumber.getText();
        address = txt_Address.getText();

        if (email.equals("")) {
            txt_Email.setBorder(BorderFactory.createLineBorder(Color.RED));
            checkFill = false;
        }

        if (phoneNumber.equals("")) {
            txt_MobileNumber.setBorder(BorderFactory.createLineBorder(Color.RED));
            checkFill = false;
        }

        if (address.equals("")) {
            txt_Address.setBorder(BorderFactory.createLineBorder(Color.RED));
            checkFill = false;
        }

        if (!checkFill) {
            JOptionPane.showMessageDialog(this, "Please Fill in All");
            return;
        }

        if (gender.equalsIgnoreCase("select.....")) {
            JOptionPane.showMessageDialog(this, "Choose 'Other' if you do not want to reveal your gender!");
            return;
        }

        try {
            Long.parseLong(phoneNumber);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter a valid phone Number");
            return;
        }

        doAddUser();
        this.dispose();
    }//GEN-LAST:event_btn_SaveActionPerformed

